/* ----------------------------------------------------------------
   unlockArtifact(artifact)
   Разблокирует артефакт и показывает модалку «получен артефакт».
   ---------------------------------------------------------------- */
function unlockArtifact(artifact) {
  // Не добавляем дважды
  if (m2State.artifacts.find(a => a.id === artifact.id)) return;

  m2State.artifacts.push(artifact);
  saveProgress();

  // Заполняем модалку
  document.getElementById('artifact-modal-icon').textContent    = artifact.icon;
  document.getElementById('artifact-modal-name').textContent    = artifact.name;
  document.getElementById('artifact-modal-preview').textContent = artifact.preview;

  openModal('modal-artifact');
}

/* ----------------------------------------------------------------
   renderNotebook()
   Рендерит содержимое блокнота (все артефакты).
   Вызывается при открытии модалки блокнота.
   ---------------------------------------------------------------- */
function renderNotebook() {
  const body = document.getElementById('notebook-body');

  if (m2State.artifacts.length === 0) {
    body.innerHTML = `
      <p style="color:var(--m2-text-muted); text-align:center; padding:20px 0;">
        Блокнот пуст. Пройди главы, чтобы собрать артефакты 📖
      </p>
    `;
    return;
  }

  body.innerHTML = m2State.artifacts.map(artifact => `
    <div class="notebook-artifact-item">
      <div class="notebook-artifact-header">
        <span class="notebook-artifact-icon">${artifact.icon}</span>
        <span class="notebook-artifact-name">${artifact.name}</span>
      </div>
      <div class="notebook-artifact-body">
        ${artifact.content}
      </div>
    </div>
  `).join('');
}

