/* ----------------------------------------------------------------
   saveProgress() / loadProgress()
   Сохраняет состояние игры в localStorage
   ---------------------------------------------------------------- */
function saveProgress() {
  const toSave = {
    energy:            m2State.energy,
    meets:             m2State.meets,
    completedChapters: m2State.completedChapters,
    artifacts:         m2State.artifacts,
    energyZoneUses:    m2State.energyZoneUses,
  };
  localStorage.setItem('m2_progress', JSON.stringify(toSave));
}

function loadProgress() {
  const raw = localStorage.getItem('m2_progress');
  if (!raw) return;
  try {
    const saved = JSON.parse(raw);
    Object.assign(m2State, saved);
  } catch(e) {
    console.warn('Не удалось загрузить прогресс Модуля 2:', e);
  }
}


