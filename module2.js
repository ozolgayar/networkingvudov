  document.getElementById('btn-trap-ok')
    .addEventListener('click', function() {
      closeModal('modal-trap');
    });

  /* ── Кнопка «В блокнот» в модалке артефакта ─────────────── */
  document.getElementById('btn-artifact-to-notebook')
    .addEventListener('click', function() {
      closeModal('modal-artifact');
      renderNotebook();
      openModal('modal-notebook');
    });

