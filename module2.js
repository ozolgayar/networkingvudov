  // показываем имя персонажа на обложке
  try {
    const m1Data = localStorage.getItem('m1_progress');
    if (m1Data) {
      const parsed   = JSON.parse(m1Data);
      const nameEl   = document.getElementById('cover-m1-name');
      const m1LinkEl = document.getElementById('cover-m1-link');

      if (parsed.playerName && nameEl) {
        nameEl.textContent = parsed.playerName + ', продолжаем!';
      }
      if (m1LinkEl) {
        m1LinkEl.textContent =
          parsed.playerName
            ? '← Модуль 1 пройден: ' + parsed.playerName
            : '';
      }
    }
  } catch(e) {
    // Нет данных М1 — ничего не делаем
  }


  /* ── Инициализация хаба ──────────────────────────────────── */
  initHub();

  console.log(
    '%c Модуль 2 загружен ✅',
    'color:#a855f7; font-weight:bold; font-size:14px'
  );

