  document.querySelectorAll('.m2-modal-overlay')
    .forEach(overlay => {
      overlay.addEventListener('click', function(e) {
        // Закрываем только если кликнули по самому оверлею,
        // а не по содержимому
        if (e.target === overlay) {
          overlay.classList.add('hidden');
        }
      });
    });

