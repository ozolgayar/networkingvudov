  document.getElementById('btn-no-energy-refuel')
    .addEventListener('click', function() {
      closeModal('modal-no-energy');
      // Запоминаем, в какую главу вернуться
      m2State.refuelReturnChapterId = m2State.currentChapterId;
      // Ищем первую доступную зону пополнения
      const availableZone = Object.keys(ENERGY_ZONES).find(zId => {
        const uses = m2State.energyZoneUses[zId] || 0;
        return uses < ENERGY_ZONES[zId].maxUses;
      });
      if (availableZone) {
        startRefuel(availableZone);
      } else {
        showToast('Все зоны питания исчерпаны!', 'error');
        showM2Screen('m2-hub');
        refreshHubZones();
      }
    });

